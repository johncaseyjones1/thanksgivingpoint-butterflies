class ReleaseModel:

    def __init__(self, speciesID, count, releaseDate):
        self.speciesID = speciesID
        self.count = count
        self.releaseDate = releaseDate

    def getSpeciesID():
        return self.speciesID

    def getCount():
        return self.count

    def getReleaseDate():
        return self.releaseDate


    def setSpeciesID(ID):
        self.speciesID = ID

    def setCount(count):
        self.count = count

    def setReleaseDate(date):
        self.releaseDate = date
